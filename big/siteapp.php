        <p>Mobile app vs responsive website  
 
 One of the first decisions a developer has when approaching a new project is whether to make a mobile application or a responsive website that fits mobile screens. According to Bankmycell.com there are about 3.8 billion smartphone users globally as of 2021. With the growing popularity of the smartphone globally it only makes sense for businesses to cater to these devices and optimize the user experience. <br> 
  
 Responsive websites are available to all users without having to download anything. This is  just a normal website in a browser that adjusts to what size screen you are using. It's also easy to add, update and fix errors to your responsive website because the users won't have to install a new version of the app every time. The downside is that the user needs to have an internet connection to run the website.<br> 
  
 With a mobile app, after the initial download the data can be stored on the device and the developer can decide which content is accessible offline. An advantage to mobile apps is they can only run on the operating system that they were built for and downloaded on, like Apple App Store or Google Play. Websites can be seen in any browser.<br> 
  
 Another advantage of mobile apps is that users can personalize the settings to their own preferences. It is also possible to make use of the device features within the app for a better user experience. Content on the app can be rearranged and tailored to the user.<br> 
  
 Mobile apps are more expensive to build and license than a regular website with mobile design. With framework programs like Foundation you can create a website that looks great in any size.  
 If you are a newer business that’s unknown it might be better to go with a responsive site at first and build up your brand. There are a lot of pluses for both options, it really comes down to the needs of the client.. 
  
  
  
  
  
  
  
 
