        <p>Galleries
When web designers are building sites, the content can become the primary focus and the visual design can often become an afterthought. But the visual aspect of a website can be just as important as the content. This means images and graphics can play a significant role in how well a website is received.  <br>
Image galleries offer designers an effective way of presenting images and content efficiently within a minimal amount of space. The galleries also become dynamic, meaning when a user hovers or clicks on the content something else will happen. Adding image galleries can be easy. This can be achieved by embedding your website with some source code and adding the images to your html file. <br> 
Lightbox is a popular type of image gallery option that is designer friendly and elegant looking for the user. When you click on an image it enlarges it, darkens the background area, and adds a border to the image. 
Adding jQuery code to a website is another great and straightforward way to present your content. It offers excellent cross-browser compatibility while also being light in the amount of code you need to run it. All you need to do is find the right jQuery plugin code for the type of gallery you want to use and copy the small bit of code to your own files in locations it specifies. <br>
The easiest way to create galleries is to use a content management system like the program WordPress. All you need to do in WordPress is find a gallery extension and upload some images into the prebuilt framework.  
Galleries are a good way to add style and effects to make a website look more unique. There are several types of galleries to pick from, it's about finding the right one for the content and what device you are designing it for. 
 
