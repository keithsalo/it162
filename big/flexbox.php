        <p>Controlling layouts on a website can be difficult and time consuming. Flexbox is a newer box layout model used to create simple and complex layouts on a website. It allows designers to control the alignment of content in numerous ways.  The spacing, sizing and ordering of elements can also be achieved using flexbox. <br> 
  
  The basic premise of flexbox is using a container that has the display property set to “flex”. This function will flex the size of all the containers within it. You can then create layouts where the individual boxes in the flexbox all work and scale in size the same way. <br>   
    
  To use the flexbox layout model, define a parent element as the “flex container.” At this point the child elements become “flex items” and will fit inside the flex container. Flexbox properties can then control the orientation, alignment, size and spacing of the child flex items. This is beneficial when it comes to creating a better responsive design for mobile and other smaller screen sizes. <br>  
    
  Flexbox also has increasingly good cross browser compatibility. Meaning it will work on most platforms a user may have. It can also simplify and clean up your code without having to use as much syntax. Flexbox is generally not good for setting up page layouts. It also has some issues with loading large content within a flexbox. Don’t go crazy and add flexbox when its needed, if you are trying to scale, align or order something. But is it a great tool that can get a lot done.  <br> 
    
    
   
   
    
   
   
   
  
   
