<h2 class="subheader">Bio</h2>
   
 <p>Kate Stannard specializes in fiction that answers the big questions of life and focuses on
lesbian romance. Her debut novel, “Crossing the Meridian,” is an interracial love story between
two high school girls that takes place in the summer of 1968, following the death of Martin
Luther King, Jr. She will be submitting it for publication in August of 2021, along with her
poetry projects.<br>
For her first creative writing project, Kate “borrowed” her journalist dad’s typewriter and
wrote a play at her backyard picnic table with two friends the summer before she turned ten.
They performed the drama about a father returning from a war for families in their
neighborhood. She also wrote poetry for much of her youth.<br>
During her professional life, Kate managed community projects and raised funds for local
nonprofit organizations. She found grant writing to be a form of storytelling as she wrote about
the challenges of the clients she served.<br>
She comes from a line of Irish storytellers, and that often informs her work. She has been
a member of writing groups for the past several years as well as studying craft at Hugo House,
where she has taken fiction and poetry courses.<br>
In March 2021, she took an intensive writing retreat with Natalie Goldberg, author of
“Writing Down the Bones,” which she has described as life changing and the boost she needed to
finish her book.</p>
